void main(){
  String week= "sun";
  switch(week) {
    case "mon":
      print("monday");
      break;
    case "tues" :
      print("tuesday");
      break;
    case "wed" :
      print("wednesday");
      break;
    case "thurs" :
      print("thursday");
      break;
    case "fri" :
      print("friday");
      break;
    case "sat" :
      print("saturday");
      break;
    default :
      print("sunday");
      break;
  }
}